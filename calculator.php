<?php
    class calculator{
            public function cong($firstnumber,$secondnumber){
                return $firstnumber+$secondnumber;
            }
            public function tru($firstnumber,$secondnumber){
                return $firstnumber-$secondnumber;

            }
            public function nhan($firstnumber,$secondnumber){
                return $firstnumber*$secondnumber;
            }
            public function chia($firstnumber,$secondnumber){
                return $firstnumber/$secondnumber;
            }

    }
?>